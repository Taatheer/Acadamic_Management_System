    private String[] checkCredentials(String username, String password) throws Exception {
        try (BufferedReader br = new BufferedReader(new FileReader("C:\\Users\\Taatheer\\Documents\\NetBeansProjects\\Assignment\\src\\AGH\\Users.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length == 4) {
                    String name = parts[0];
                    String id = parts[1].trim();
                    String encrpass = parts[2].trim();
                    String type = parts[3].trim();
                    
                    //Decrypt the password
                    String decrPass = EncryptionUtil.decrypt(encrpass);
                    
                    if (username.equals(name) && password.equals(decrPass)) {
                        return new String[] { type, id }; // Return user type and ID
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null;
    }

    private void openUserFrame(String userType, String userId) throws IOException {
        JFrame userFrame = null;
        switch (userType) {
            case "Student":
                userFrame = new StudentMainJFRame(userId);
                break;
            case "Lecturer":
                // Call a method to check the lecturer's access level
                String accessLevel = checkLecturerAccess(userId);
                if (accessLevel == null) {
                    JOptionPane.showMessageDialog(this, "Access denied", "Access Denied", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if(accessLevel == "Supervisor"){
                    userFrame = new LecturerJFrame(userId);
                }else
                    userFrame = new SecondMarkerJFrame(userId);
                break;
            case "Project Manager":
                String accessLevel1 = checkLecturerAccess(userId);
                if ("Supervisor".equals(accessLevel1) || "Second Marker".equals(accessLevel1)) {
                    String[] options = {"Project Manager", accessLevel1};
                    int choice = JOptionPane.showOptionDialog(
                        null,
                        "Choose your access level:",
                        "Access Level",
                        JOptionPane.DEFAULT_OPTION,
                        JOptionPane.INFORMATION_MESSAGE,
                        null,
                        options,
                        options[0]
                    );

                    if (choice == 0) {
                        userFrame = new ProjectManagerJFrame();
                    } else if (choice == 1) {
                        if ("Supervisor".equals(accessLevel1)) {
                            userFrame = new LecturerJFrame(userId);
                        } else if ("Second Marker".equals(accessLevel1)) {
                            userFrame = new SecondMarkerJFrame(userId);
                        }
                    }
                } else {
                    userFrame = new ProjectManagerJFrame();
                }
                break;
            case "Admin":
                userFrame = new AdminMainJFrame();
                break;
            default:
                throw new IllegalStateException("Unexpected user type: " + userType);
        }
        userFrame.setVisible(true);
        this.dispose(); // Close the login frame
    }

    // Method to check lecturer access
    private String checkLecturerAccess(String lecturerId) {
        String filePath = "C:\\Users\\Taatheer\\Documents\\NetBeansProjects\\Assignment\\src\\AGH\\lecturers.txt";
        try (BufferedReader br = new BufferedReader(new FileReader(filePath))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                if (data.length == 10 && data[3].trim().equals(lecturerId)) {
                    if (Boolean.parseBoolean(data[8].trim())) {
                        return "Supervisor";
                    } else if (Boolean.parseBoolean(data[9].trim())) {
                        return "Second Marker";
                    } else {
                        return null; // Access denied
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return null; // Access denied
    }

