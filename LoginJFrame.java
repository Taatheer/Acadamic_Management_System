    private void butLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butLogInActionPerformed
        String username = txtUsername.getText().trim();
        String password = new String(txtPass.getPassword()).trim();
        String[] userdetails = null;
        try {
            userdetails = checkCredentials(username,password);
        } catch (Exception ex) {
            Logger.getLogger(LoginJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }


        if (userdetails == null) {
            JOptionPane.showMessageDialog(this, "Invalid username or password", "Login Failed", JOptionPane.ERROR_MESSAGE);
        } else {
            String userType = userdetails[0];
            String userID = userdetails[1];
            JOptionPane.showMessageDialog(this, "Login successful", "Login", JOptionPane.INFORMATION_MESSAGE);
            try {
                openUserFrame(userType,userID);
            } catch (IOException ex) {
                Logger.getLogger(LoginJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_butLogInActionPerformed

